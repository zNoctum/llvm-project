#ifndef LLVM_LIB_TARGET_B3_INSTPRINTER_B3INSTPRINTER_H
#define LLVM_LIB_TARGET_B3_INSTPRINTER_B3INSTPRINTER_H

#include "llvm/ADT/DenseSet.h"
#include "llvm/MC/MCInstPrinter.h"


namespace llvm {
class B3InstPrinter : public MCInstPrinter {
  void printCall(const MCInst *MI, unsigned Skip, raw_ostream &O);
public:
  using MCInstPrinter::MCInstPrinter;

  void printInst(const MCInst *MI, uint64_t Address, StringRef Annot,
                 const MCSubtargetInfo &STI, raw_ostream &OS) override;
  void printOperand(const MCInst *MI, unsigned OpNo, raw_ostream &O,
                    const char *Modifier = nullptr);

  void printStringImm(const MCInst *MI, unsigned OpNo, raw_ostream &O);

  // Autogenerated by tblgen.
  std::pair<const char *, uint64_t> getMnemonic(const MCInst *MI) override;
  void printInstruction(const MCInst *MI, uint64_t Address, raw_ostream &O);
  static const char *getRegisterName(MCRegister Reg);
};
} // namespace llvm

#endif // LLVM_LIB_TARGET_B3_INSTPRINTER_B3INSTPRINTER_H
